public class Operator {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;

        a+=5;
        System.out.println(a);

        b-=1;
        System.out.println(b);
        b*=2;
        System.out.println(b);
        b/=3;
        System.out.println(b);
        b%=4;
        System.out.println(b);
    }
}
